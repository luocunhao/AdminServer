package com.pulan.model;

public class Advertisment {
	private String media_id;
	private String media_name;
	private String media_type;
	private String media_link;
	private String media_subject;
	public Advertisment(){}
	public Advertisment(String media_id, String media_name, String media_type, String media_link,
			String media_subject) {
		this.media_id = media_id;
		this.media_name = media_name;
		this.media_type = media_type;
		this.media_link = media_link;
		this.media_subject = media_subject;
	}
	public Advertisment(String media_name, String media_type, String media_link,
			String media_subject) {
		this.media_name = media_name;
		this.media_type = media_type;
		this.media_link = media_link;
		this.media_subject = media_subject;
	}
	public String getMedia_id() {
		return media_id;
	}
	public void setMedia_id(String media_id) {
		this.media_id = media_id;
	}
	public String getMedia_name() {
		return media_name;
	}
	public void setMedia_name(String media_name) {
		this.media_name = media_name;
	}
	public String getMedia_type() {
		return media_type;
	}
	public void setMedia_type(String media_type) {
		this.media_type = media_type;
	}
	public String getMedia_link() {
		return media_link;
	}
	public void setMedia_link(String media_link) {
		this.media_link = media_link;
	}
	public String getMedia_subject() {
		return media_subject;
	}
	public void setMedia_subject(String media_subject) {
		this.media_subject = media_subject;
	}
	@Override
	public String toString() {
		return "Advertisment [media_id=" + media_id + ", media_name=" + media_name + ", media_type=" + media_type
				+ ", media_link=" + media_link + ", media_subject=" + media_subject + "]";
	}
	
	
}
