package com.pulan.model;

public class VerbalTrick {
	private int id;
	private String type;
	private String category;
	private String content;
	private String create_time;
	private String create_name;
	private String type_desc;
	private String category_desc;
	public VerbalTrick(){
		
	}
	public VerbalTrick(String type, String category, String content, String create_time, String create_name,
			String type_desc, String category_desc) {
		this.type = type;
		this.category = category;
		this.content = content;
		this.create_time = create_time;
		this.create_name = create_name;
		this.type_desc = type_desc;
		this.category_desc = category_desc;
	}
	public VerbalTrick(int id, String type, String category, String content, String create_time, String create_name,
			String type_desc, String category_desc) {
		this.id = id;
		this.type = type;
		this.category = category;
		this.content = content;
		this.create_time = create_time;
		this.create_name = create_name;
		this.type_desc = type_desc;
		this.category_desc = category_desc;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getCreate_time() {
		return create_time;
	}
	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}
	public String getCreate_name() {
		return create_name;
	}
	public void setCreate_name(String create_name) {
		this.create_name = create_name;
	}
	public String getType_desc() {
		return type_desc;
	}
	public void setType_desc(String type_desc) {
		this.type_desc = type_desc;
	}
	public String getCategory_desc() {
		return category_desc;
	}
	public void setCategory_desc(String category_desc) {
		this.category_desc = category_desc;
	}
	@Override
	public String toString() {
		return "VerbalTrick [id=" + id + ", type=" + type + ", category=" + category + ", content=" + content
				+ ", create_time=" + create_time + ", create_name=" + create_name + ", type_desc=" + type_desc
				+ ", category_desc=" + category_desc + "]";
	}
	
}
